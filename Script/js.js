//valhalla button
$(document).ready(function()
{
$(".show").click(function()
{
$(".dataval") .show(); 
$(".hide").show();
$(".show").hide();
});
$(".hide").click(function()
{
$(".dataval") .hide(); 
$(".hide").hide();
$(".show").show();
});	
});

